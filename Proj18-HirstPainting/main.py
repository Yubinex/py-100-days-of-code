import turtle as t
import random

color_list = [
    (243, 242, 239), (171, 158, 34), (99, 6, 51),
    (75, 94, 172), (246, 239, 243), (231, 209, 76),
    (10, 35, 127), (177, 105, 154), (104, 122, 209),
    (213, 90, 35), (239, 245, 241), (236, 238, 246),
    (26, 97, 41), (33, 102, 47), (119, 134, 207),
    (218, 91, 43), (182, 125, 162), (202, 164, 86),
    (115, 20, 52), (128, 75, 100), (12, 31, 103),
    (175, 183, 222), (137, 171, 142), (217, 175, 194),
    (107, 151, 98), (24, 72, 36), (239, 176, 150),
    (173, 206, 176)
]

t.colormode(255)
tim = t.Turtle()
tim.penup()

y_pos = -300
while y_pos < 300:
    tim.setposition(-300, y_pos)
    for _ in range(10):
        tim.forward(50)
        tim.dot(20, random.choice(color_list))
    y_pos += 50

screen = t.Screen()
screen.exitonclick()
